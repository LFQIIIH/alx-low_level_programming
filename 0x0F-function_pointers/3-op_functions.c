#include "3-calc.h"

/**
 * op_add - add opration of a and b
 *
 * @a: number
 * @b: number
 *
 * Return: the result of the operation
 */
int	op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - subtraction operation of a and b
 *
 * @a: number
 * @b: number
 *
 * Return: the result of the operation
 */
int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - multiplication oper of a and b
 *
 * @a: number
 * @b: number
 *
 * Return: the result of the operation
 */
int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - division oper of a and b
 *
 * @a: number
 * @b: number
 *
 * Return: the result of the operation
 */
int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - modulo oper of a and b
 *
 * @a: number
 * @b: number
 *
 * Return: the result of the operation
 */
int op_mod(int a, int b)
{
	return (a % b);
}

